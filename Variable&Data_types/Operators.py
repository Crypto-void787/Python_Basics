
a = 8 
b = 4 

print("a =", a)
print("b =", b)
""" Arithematice Operators ( +, - , / , * , % , // , ** ) """

print("\nArithematic Operators!")
print("a+b =", a+b)
print("a-b =",a-b)
print("a/b =",a/b)
print("a*b =",a*b)
print("a%b =",a%b)
print("a//b =",a//b)
print("a**b =",a**b)

''' Comparison operators ( <, >, <=, >= , !=, == )'''

print("\nComparative Operators")
print("a < b =" , a < b  )
print("a > b =" , a > b  )
print("a <= b =" , a <= b  )
print("a >= b =" , a >= b  )
print("a != b =" , a != b  )
print("a == b =" , a == b  )

""" Assignment Operators ( ==, +=, -=, /=, *=, **=, %= )"""

print("\nAssignment Operators")
x = 10
print("Initial x =", x)

x += 5
print("x += 5 =", x)

x -= 3
print("x -= 3 =", x)

x *= 2
print("x *= 2 =", x)

x /= 4
print("x /= 4 =", x)

x **= 2
print("x **= 2 =", x)

x %= 5
print("x %= 5 =", x)

''' Logical Operators ( and , or , not )'''

print("\nLogical Operators")
p = True
q = False

print("p and q =", p and q)
print("p or q =", p or q)
print("not p =", not p)

