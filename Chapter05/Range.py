'''Range used in for loops as range(start, stop, step)
   first tell frm where to start actually n then where
     to stop n how steps are to ignore during this whole '''

for i in range(10) :  #jus a general RANGE n printing all in 
    print(i)
print("Loop ended\n")


for val in range(0, 5 ) : # range w starting n ending number 
    print(val)
print("Loop ended\n")

for v in range(0, 50, 5): #range w starting n ending but also about skipping steps 
    print(v)

